function resolveURL(){
	var search = location.search;
	search = search.substring(1,search.length);
	var obj = {};
	if(search.indexOf('&')>0){
		var searchArr = search.split('&');
		obj[searchArr[0].split('=')[0]] = searchArr[0].split('=')[1];
		obj[searchArr[1].split('=')[0]] = searchArr[1].split('=')[1];
	}else{
		obj[search.split('=')[0]] = search.split('=')[1];
	}
	return obj
}